    // write_buf[0]=0b00010001;
    // write_buf[1]=0b00000001;
    // write_buf[2]=0b00010001;
    // busWriteBuf(busDev, G_TRIG_1, &write_buf[0],2);
    // delay(1);
    //seems like page swtiching have issues
    busReadBuf(busDev,0x32,&result[0],2);
